#include <stdio.h>
#include <stdlib.h>
#include "calculator.h"

// Function Definitions


/* 
 * ===  FUNCTION  ======================================================================
 *         Name:  Add_Numbers
 *  Description:  Add two numbers
 *  Param: num1:
 *  Param: num2:
 * =====================================================================================
 */

int Add_Numbers(int num1, int num2) {

    return num1 + num2;
}


/* 
 * ===  FUNCTION  ======================================================================
 *         Name:  Sub_Numbers
 *  Description:  Subtract two numbers
 *  Param: num1:
 *  Param: num2:
 * =====================================================================================
 */
int Sub_Numbers(int num1, int num2) {

    return num1 - num2;
}

